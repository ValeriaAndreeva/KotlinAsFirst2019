 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun squareSequenceDigit(n: Int): Int {
    var s = 0
    var m = 2
    var l = 1
    if (n == 1) return n
    else while (l < n) {
        s = sqr(m)
        m++
        l += digitNumber(s)
    }
    return if (l != n) {
        while (l != n) {
            s /= 10
            l--
        }
        s % 10
    }
    else s % 10
}
